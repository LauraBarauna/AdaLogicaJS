const numero1 = 100;
const numero2 = 20;

// Condicionais if's
if (numero1 > 1) {
    console.log(`Este número é maior que 1`);
}

if (numero1 > 200) {
    console.log(`Este número é maior que 200`);
} else {
    console.log(`Este número é menor ou igual a 200`);
}

if(numero2 < numero1) {
    console.log(`${numero2} é menor que ${numero1}`);
}

if(numero2 <= numero1) {
    console.log(`${numero2} é menor ou igual que ${numero1}`);
}

if(numero1 === numero2) {
    console.log(`Os números ${numero1} e ${numero2} são iguais`);
}
